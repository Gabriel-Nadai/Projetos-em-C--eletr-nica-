#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Pular a linha \n

    printf("Eu sou o Code::Block.\n");

    // \t mesma coisa do TAB

    printf ("\tEu sou o Code::Block.\n");

    // \nao colocar duas barras \\ -> imprimir uma barra

    printf ("Eu sou \\nao sou o Code::Block.\n");

    // \" -> para imprimir uma aspas Dupla
    // \' -> para imprimir uma aspas simples
    // \0 -> aqui finaliza o nosso texto



    return 0;
}
